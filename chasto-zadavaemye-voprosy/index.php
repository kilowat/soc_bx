 <b>Вопрос: На каких условиях осуществляется социальное обслуживание на дому? </b><br>
 <i>Ответ:</i> Социальное обслуживание на дому предоставляется на условиях за плату, если на дату обращения среднедушевой&nbsp; доход гражданина, превышает&nbsp; полуторную величину прожиточного минимума, установленную в Краснодарском крае. <br>
 Социальное обслуживание на дому предоставляется на условиях бесплатно: <br>
<ul>
	<li>
	если на дату&nbsp; обращения среднедушевой доход&nbsp; гражданина ниже или равен&nbsp; полуторной величине прожиточного минимума, установленного в Краснодарском крае; </li>
	<li>
	несовершеннолетним детям; </li>
	<li>
	лицам, пострадавшим в результате чрезвычайных ситуаций, вооруженных межнациональных (межэтнических) конфликтов; </li>
	<li>лицам, подвергшимся физическому и психическому насилию, потерявшим жилье или работу, оказавшимся в экстремальных психологических и социально-бытовых &nbsp;условиях; </li>
	<li>
	участникам и инвалидам Великой Отечественной войны. </li>
</ul>
