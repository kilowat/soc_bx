 <b>Вопрос: Как определяется&nbsp; индивидуальная потребность гражданина в социальных услугах? </b><br>
 <i>Ответ:</i> Для определения индивидуальной потребности гражданина в социальных услугах организуется обследование условий обеспечения жизнедеятельности гражданина не позднее 2 рабочих дней с момента получения заявления и необходимых документов. <br>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Осуществление процедуры оценки&nbsp; жизнедеятельности гражданина: <br>
<ul>
	<li>
	ознакомление гражданина с порядком признания нуждающимся в социальном обслуживании; </li>
	<li>
	ознакомление гражданина с перечнем социальных услуг, порядком и условиями их предоставления; </li>
	<li>
	оформление акта определения индивидуальной потребности гражданина в социальных услугах. </li>
	<li>
	подготовка проекта индивидуальной программы предоставления социальных услуг гражданину. </li>
</ul>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Заявление гражданина с приобщенными&nbsp; документами направляются на рассмотрение комиссии&nbsp; по определению нуждаемости граждан. <br>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Рассмотрение результатов определения индивидуальной потребности в предоставлении социальных услуг на дому проводится комиссией в течение 1 рабочего дня с момента поступления в комиссию материала. <br>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Руководитель УСЗН принимает решение о признании гражданина нуждающимся в социальном обслуживании на дому либо об отказе, издает соответствующий приказ. <br>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Решение о признании гражданина нуждающимся в социальном обслуживании на дому принимается в течение 5 рабочих дней со дня подачи гражданином заявления. О принятом решении заявитель информируется в письменной форме. <br>
